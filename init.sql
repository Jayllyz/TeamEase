CREATE DATABASE IF NOT EXISTS teamease;

CREATE TABLE COMPANY
(
  siret CHAR(14) NOT NULL,
  companyName VARCHAR(255) NOT NULL,
  email VARCHAR(255) NOT NULL,
  address VARCHAR(255) NOT NULL,
  password VARCHAR(255) NOT NULL,
  rights INT NOT NULL,
  PRIMARY KEY (siret)
);

CREATE TABLE ESTIMATE
(
  id INT NOT NULL,
  amount INT NOT NULL,
  creationDate DATE NOT NULL,
  siret CHAR(14) NOT NULL,
  PRIMARY KEY (id),
  FOREIGN KEY (siret) REFERENCES COMPANY(siret)
);

CREATE TABLE INVOICE
(
  id INT NOT NULL,
  amount INT NOT NULL,
  paymentDay DATE NOT NULL,
  siret CHAR(14) NOT NULL,
  PRIMARY KEY (id),
  FOREIGN KEY (siret) REFERENCES COMPANY(siret)
);

CREATE TABLE MATERIAL
(
  id INT NOT NULL,
  type VARCHAR(255) NOT NULL,
  PRIMARY KEY (id)
);

CREATE TABLE CATEGORY
(
  id INT NOT NULL,
  name VARCHAR(255) NOT NULL,
  PRIMARY KEY (id)
);

CREATE TABLE SERVICE
(
  id INT NOT NULL,
  siret CHAR(14) NOT NULL,
  PRIMARY KEY (id),
  FOREIGN KEY (siret) REFERENCES COMPANY(siret)
);

CREATE TABLE ACTIVITY
(
  id INT NOT NULL,
  maxAttendee INT NOT NULL,
  date DATE NOT NULL,
  duration INT NOT NULL,
  priceAttendee INT NOT NULL,
  name VARCHAR(255) NOT NULL,
  description TEXT NOT NULL,
  id_activity INT NOT NULL,
  PRIMARY KEY (id),
  FOREIGN KEY (id_activity) REFERENCES SERVICE(id)
);

CREATE TABLE RESERVATION
(
  id INT NOT NULL,
  attendee INT NOT NULL,
  id_activity INT NOT NULL,
  siret CHAR(14) NOT NULL,
  PRIMARY KEY (id),
  FOREIGN KEY (id_activity) REFERENCES ACTIVITY(id),
  FOREIGN KEY (siret) REFERENCES COMPANY(siret)
);

CREATE TABLE ROOM
(
  id INT NOT NULL,
  number INT NOT NULL,
  address DATE NOT NULL,
  id_activity INT NOT NULL,
  PRIMARY KEY (id),
  FOREIGN KEY (id_activity) REFERENCES ACTIVITY(id)
);

CREATE TABLE MATERIAL_ROOM
(
  quantity INT NOT NULL,
  id_room INT NOT NULL,
  id_material INT NOT NULL,
  FOREIGN KEY (id_room) REFERENCES ROOM(id),
  FOREIGN KEY (id_material) REFERENCES MATERIAL(id)
);

CREATE TABLE MATERIEL_ACTIVITE
(
  quantite INT NOT NULL,
  id_activity INT NOT NULL,
  id_material INT NOT NULL,
  FOREIGN KEY (id_activity) REFERENCES ACTIVITY(id),
  FOREIGN KEY (id_material) REFERENCES MATERIAL(id)
);

CREATE TABLE BELONG
(
  id_activity INT NOT NULL,
  id_category INT NOT NULL,
  FOREIGN KEY (id_activity) REFERENCES ACTIVITY(id),
  FOREIGN KEY (id_category) REFERENCES CATEGORY(id)
);
